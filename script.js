// Initialize language on page load
document.addEventListener('DOMContentLoaded', function () {
    // Set default language from localStorage or use 'es'
    const savedLanguage = localStorage.getItem('language') || 'es';
    setLanguage(savedLanguage);

    // Add event listener to language selector
    const languageSelect = document.getElementById('language-select');
    languageSelect.value = savedLanguage;
    languageSelect.addEventListener('change', function () {
        const selectedLanguage = this.value;
        setLanguage(selectedLanguage);
        localStorage.setItem('language', selectedLanguage);
    });

    // Add scroll effect to header
    window.addEventListener('scroll', function () {
        const header = document.querySelector('header');
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // Handle hover effect on expandable service cards
    const expandableCards = document.querySelectorAll('.expandable-card');
    expandableCards.forEach(card => {
        const body = card.querySelector('.expandable-body');
        let hoverTimer;

        card.addEventListener('mouseenter', () => {
            if (!card.classList.contains('show-long')) {
                // Lock the height to precisely current height before changing text
                const currentHeight = body.getBoundingClientRect().height;
                body.style.height = `${currentHeight}px`;
            }
            
            hoverTimer = setTimeout(() => {
                card.classList.add('show-long');
                body.classList.add('scrollable');
            }, 2000);
        });

        card.addEventListener('mouseleave', () => {
            clearTimeout(hoverTimer);
            card.classList.remove('show-long');
            body.classList.remove('scrollable');
            // Remove fixed height slightly after to avoid jumping
            setTimeout(() => {
                if (!card.classList.contains('show-long')) {
                    body.style.height = '';
                }
            }, 50);
        });
    });

    // Initialize hero background slider
    initHeroSlider();

    // Contact form submission
    const contactForm = document.getElementById('contactForm');
    if (contactForm) {
        contactForm.addEventListener('submit', function (e) {
            e.preventDefault();

            // Get form data
            const formData = new FormData(this);
            const data = Object.fromEntries(formData);

            // Simple validation
            if (!data.name || !data.email || !data.service || !data.message) {
                alert(getTranslation('contact.form.submit') === 'Enviar Consulta' ?
                    'Por favor complete todos los campos requeridos.' :
                    getTranslation('contact.form.submit') === 'Send Consultation' ?
                        'Please fill in all required fields.' :
                        '请填写所有必填字段。');
                return;
            }

            // Cambiar el texto del botón mientras se envía
            const submitBtn = this.querySelector('button[type="submit"]');
            const originalText = submitBtn.textContent;
            submitBtn.textContent = 'Enviando...';
            submitBtn.disabled = true;

            // Send data using FormSubmit AJAX
            fetch("https://formsubmit.co/ajax/info@tacvzla.com", {
                method: "POST",
                headers: {
                    'Content-Type': 'application/json',
                    'Accept': 'application/json'
                },
                body: JSON.stringify(data)
            })
                .then(response => response.json())
                .then(data => {
                    alert(getTranslation('contact.form.submit') === 'Enviar Consulta' ?
                        '¡Gracias por su consulta! Nos pondremos en contacto pronto.' :
                        getTranslation('contact.form.submit') === 'Send Consultation' ?
                            'Thank you for your inquiry! We will contact you soon.' :
                            '感谢您的咨询！我们会尽快与您联系。');
                    this.reset();
                })
                .catch(error => {
                    console.error('Error:', error);
                    alert('Hubo un error al enviar el formulario. Por favor, intente de nuevo más tarde.');
                })
                .finally(() => {
                    submitBtn.textContent = originalText;
                    submitBtn.disabled = false;
                });
        });
    }
});

// Hero background fade slider functionality
function initHeroSlider() {
    const layers = document.querySelectorAll('.hero-bg-layer');
    if (!layers.length) return;

    let currentIndex = 0;
    const totalImages = layers.length;

    function nextSlide() {
        const currentLayer = layers[currentIndex];
        const nextIndex = (currentIndex + 1) % totalImages;
        const nextLayer = layers[nextIndex];

        currentLayer.classList.remove('active');
        nextLayer.classList.add('active');

        currentIndex = nextIndex;
        setTimeout(nextSlide, 5000);
    }

    setTimeout(nextSlide, 5000);
}

// Function to set language
function setLanguage(language) {
    // Update HTML lang attribute
    document.documentElement.lang = language;

    // Update body class for Chinese font support
    if (language === 'zh') {
        document.body.setAttribute('lang', 'zh');
    } else {
        document.body.removeAttribute('lang');
    }

    // Update all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[language] && translations[language][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[language][key];
            } else if (element.tagName === 'OPTION') {
                element.textContent = translations[language][key];
            } else {
                element.textContent = translations[language][key];
            }
        }
    });

    // Update select options text
    updateSelectOptions(language);
}

// Function to get translation (for form validation messages)
function getTranslation(key) {
    const currentLanguage = document.documentElement.lang || 'es';
    return translations[currentLanguage][key] || key;
}

// Function to update select options text
function updateSelectOptions(language) {
    const select = document.getElementById('service');
    const options = select.querySelectorAll('option');

    options.forEach(option => {
        const key = option.getAttribute('data-i18n');
        if (key && translations[language] && translations[language][key]) {
            option.textContent = translations[language][key];
        }
    });
}